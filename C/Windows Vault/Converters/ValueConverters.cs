    public class RatingToVisibilityConverter : IValueConverter
    {
        public object Convert(object value, Type targetType, object parameter, CultureInfo culture)
        {
            if (value is double rating)
            {
                // If no parameter is provided, show if rating > 0
                if (parameter == null)
                {
                    return rating > 0 ? Visibility.Visible : Visibility.Collapsed;
                }

                // If parameter is provided, check if rating >= parameter value
                if (int.TryParse(parameter.ToString(), out int threshold))
                {
                    return rating >= threshold ? Visibility.Visible : Visibility.Collapsed;
                }
            }
            return Visibility.Collapsed;
        }

        public object ConvertBack(object value, Type targetType, object parameter, CultureInfo culture)
        {
            throw new NotImplementedException();
        }
    }

